def extract_payload_indicators(event_profile):
    text = event_profile["combined_text"].lower()
    tags = set(event_profile["all_tags"])

    indicators = {
        "credential_prompt": False,
        "plaintext_remote_login": False,
        "command_execution": False,
        "sql_injection": False,
        "path_traversal": False,
        "shell_execution": False,
        "beaconing_pattern": False,
        "telnet_session": False,
        "evidence_items": []
    }

    if "telnet_login_prompt" in tags or "telnet_password_prompt" in tags:
        indicators["credential_prompt"] = True
        indicators["evidence_items"].append("Credential prompt tags detected.")

    if "telnet_data" in tags:
        indicators["plaintext_remote_login"] = True
        indicators["telnet_session"] = True
        indicators["evidence_items"].append("Telnet application data detected.")

    if "possible_beaconing" in tags:
        indicators["beaconing_pattern"] = True
        indicators["evidence_items"].append("Beaconing tag detected.")

    if "possible_sql_injection" in tags or "suspicious_http_payload" in tags:
        indicators["sql_injection"] = True
        indicators["evidence_items"].append("SQL injection / suspicious HTTP tag detected.")

    if "possible_path_traversal" in tags:
        indicators["path_traversal"] = True
        indicators["evidence_items"].append("Path traversal tag detected.")

    if "possible_command_execution" in tags:
        indicators["command_execution"] = True
        indicators["shell_execution"] = True
        indicators["evidence_items"].append("Command execution tag detected.")

    text_rules = [
        ("credential_prompt", r"\blogin:\b", "Login prompt text observed."),
        ("credential_prompt", r"\bpassword:\b", "Password prompt text observed."),
        ("telnet_session", r"\blast login:", "Interactive login session evidence observed."),
        ("command_execution", r"/sbin/ping", "Command execution evidence: /sbin/ping"),
        ("command_execution", r"\bping\s+[A-Za-z0-9\.\-]+", "Ping command text observed."),
        ("sql_injection", r"union\s+select", "SQL injection text observed."),
        ("sql_injection", r"or\s+1=1", "SQL injection boolean pattern observed."),
        ("path_traversal", r"\.\./", "Path traversal text observed."),
        ("shell_execution", r"/bin/sh", "Shell execution text observed."),
        ("shell_execution", r"cmd\.exe", "cmd.exe text observed."),
        ("shell_execution", r"powershell", "PowerShell text observed."),
    ]

    for key, pattern, msg in text_rules:
        if re.search(pattern, text, flags=re.IGNORECASE):
            indicators[key] = True
            indicators["evidence_items"].append(msg)

    if indicators["shell_execution"]:
        indicators["command_execution"] = True

    return indicators

