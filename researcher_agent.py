def score_mitre_match(event_profile, indicators, mitre_item):
    score = 0
    evidence = []

    event_tags = set(event_profile["all_tags"])
    text = event_profile["combined_text"].lower()

    for tag in mitre_item["tags"]:
        if tag in event_tags:
            score += 3
            evidence.append(f"Tag match: {tag}")

    for kw in mitre_item["keywords"]:
        if kw.lower() in text:
            score += 2
            evidence.append(f"Keyword match: {kw}")

    if mitre_item["id"] == "T1021":
        if indicators["telnet_session"]:
            score += 3
            evidence.append("Indicator bonus: telnet session detected")
        if indicators["credential_prompt"]:
            score += 2
            evidence.append("Indicator bonus: credential prompt detected")
        if indicators["plaintext_remote_login"]:
            score += 2
            evidence.append("Indicator bonus: plaintext remote login evidence")

    if mitre_item["id"] == "T1059":
        if indicators["command_execution"]:
            score += 3
            evidence.append("Indicator bonus: command execution evidence")
        if indicators["shell_execution"]:
            score += 2
            evidence.append("Indicator bonus: shell execution evidence")

    if mitre_item["id"] == "T1190":
        if indicators["sql_injection"]:
            score += 3
            evidence.append("Indicator bonus: SQL injection evidence")
        if indicators["path_traversal"]:
            score += 3
            evidence.append("Indicator bonus: path traversal evidence")

    if mitre_item["id"] == "T1071":
        if indicators["beaconing_pattern"]:
            score += 3
            evidence.append("Indicator bonus: beaconing pattern detected")

    if mitre_item["id"] == "T1046":
        if "tcp_syn" in event_tags:
            score += 2
            evidence.append("Indicator bonus: SYN activity detected")

    return score, evidence


def retrieve_mitre_candidates(event_profile, indicators, mitre_db):
    candidates = []

    for item in mitre_db:
        score, evidence = score_mitre_match(event_profile, indicators, item)
        if score > 0:
            candidates.append({
                "id": item["id"],
                "name": item["name"],
                "description": item["description"],
                "attack_stage": item["attack_stage"],
                "score": score,
                "evidence": evidence
            })

    candidates.sort(key=lambda x: x["score"], reverse=True)
    return candidates

def retrieve_protocol_risks(event_profile, protocol_risk_db):
    tags = set(event_profile["all_tags"])
    risks = []

    for item in protocol_risk_db:
        matched = [cond for cond in item["conditions"] if cond in tags]
        if matched:
            risks.append({
                "protocol_family": item["protocol_family"],
                "risk": item["risk"],
                "severity": item["severity"],
                "description": item["description"],
                "matched_conditions": matched
            })

    return risks

def estimate_risk_level(mitre_candidates, protocol_risks, indicators):
    score = 0

    if mitre_candidates:
        score += mitre_candidates[0]["score"]

    for r in protocol_risks:
        if r["severity"] == "high":
            score += 3
        elif r["severity"] == "medium":
            score += 2
        else:
            score += 1

    if indicators["credential_prompt"]:
        score += 2
    if indicators["plaintext_remote_login"]:
        score += 2
    if indicators["command_execution"]:
        score += 2
    if indicators["sql_injection"]:
        score += 3
    if indicators["path_traversal"]:
        score += 3
    if indicators["beaconing_pattern"]:
        score += 2

    if score >= 12:
        return "high"
    elif score >= 6:
        return "medium"
    else:
        return "low"

def generate_reasoning(event_profile, indicators, mitre_candidates, protocol_risks):
    reasons = []

    if indicators["telnet_session"]:
        reasons.append("The traffic appears to include an interactive Telnet session.")
    if indicators["credential_prompt"]:
        reasons.append("Login and/or password prompts were observed, suggesting credential entry during the session.")
    if indicators["plaintext_remote_login"]:
        reasons.append("Human-readable application data suggests plaintext session content.")
    if indicators["command_execution"]:
        reasons.append("Command-like content was detected after session establishment.")
    if indicators["sql_injection"]:
        reasons.append("Payload content includes patterns associated with SQL injection.")
    if indicators["path_traversal"]:
        reasons.append("Payload content includes path traversal indicators.")
    if indicators["beaconing_pattern"]:
        reasons.append("Timing behavior suggests periodic communication that may indicate beaconing.")

    if mitre_candidates:
        top = mitre_candidates[0]
        reasons.append(f"The strongest ATT&CK mapping is {top['id']} - {top['name']}.")

    for r in protocol_risks[:2]:
        reasons.append(f"Protocol risk identified: {r['risk']}.")

    return " ".join(reasons)

def build_research_output(event_profile):
    indicators = extract_payload_indicators(event_profile)
    mitre_candidates = retrieve_mitre_candidates(event_profile, indicators, MITRE_DB)
    protocol_risks = retrieve_protocol_risks(event_profile, PROTOCOL_RISK_DB)
    risk_level = estimate_risk_level(mitre_candidates, protocol_risks, indicators)
    reasoning = generate_reasoning(event_profile, indicators, mitre_candidates, protocol_risks)

    evidence = []
    evidence.extend(indicators["evidence_items"])

    if mitre_candidates:
        evidence.extend(mitre_candidates[0]["evidence"])

    top_threat = mitre_candidates[0] if mitre_candidates else None

    output = {
        "flow_key": event_profile["flow_key"],
        "packet_count": event_profile["packet_count"],
        "total_bytes": event_profile["total_bytes"],
        "top_threat": top_threat,
        "threat_candidates": mitre_candidates[:5],
        "protocol_risks": protocol_risks,
        "risk_level": risk_level,
        "indicators": indicators,
        "evidence": evidence,
        "reasoning": reasoning,
        "recommended_focus": []
    }

    if indicators["credential_prompt"]:
        output["recommended_focus"].append("credential exposure")
    if indicators["plaintext_remote_login"]:
        output["recommended_focus"].append("plaintext remote access")
    if indicators["command_execution"]:
        output["recommended_focus"].append("post-login command execution")
    if indicators["sql_injection"] or indicators["path_traversal"]:
        output["recommended_focus"].append("application exploitation")
    if indicators["beaconing_pattern"]:
        output["recommended_focus"].append("periodic callback investigation")

    return output

