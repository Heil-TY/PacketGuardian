def dedup_keep_order(items):
    seen = set()
    out = []
    for x in items:
        key = str(x)
        if key not in seen:
            seen.add(key)
            out.append(x)
    return out

def clean_payload_previews(payload_previews):
    cleaned = []

    for p in payload_previews:
        if not p:
            continue

        text = str(p).strip()
        if not text:
            continue

        if re.search(r"[A-Za-z0-9]", text):
            printable_chars = sum(ch.isalnum() or ch in " .:/_-@$()[]=+" for ch in text)
            ratio = printable_chars / max(len(text), 1)

            if ratio >= 0.30:
                cleaned.append(text)

    seen = set()
    result = []
    for x in cleaned:
        if x not in seen:
            seen.add(x)
            result.append(x)

    return result


def clean_printable_strings(strings):
    cleaned = []

    for s in strings:
        if not s:
            continue

        text = str(s).strip()
        if len(text) < 3:
            continue

        if re.search(r"[A-Za-z0-9]", text):
            cleaned.append(text)

    seen = set()
    result = []
    for x in cleaned:
        if x not in seen:
            seen.add(x)
            result.append(x)

    return result

def build_event_profile(flow, packets_for_flow):
    flow_tags = flow.get("tags", [])
    flow_observations = flow.get("observations", [])
    intervals = flow.get("intervals", [])

    packet_tags = []
    packet_observations = []
    raw_payload_previews = []
    raw_printable_strings = []
    tcp_flags = []

    for pkt in packets_for_flow:
        packet_tags.extend(pkt.get("semantic_tags", []))
        packet_observations.extend(pkt.get("observations", []))

        preview = pkt.get("payload_preview", "")
        if preview and str(preview).strip():
            raw_payload_previews.append(preview)

        raw_printable_strings.extend(pkt.get("printable_strings", []))
        tcp_flags.extend(pkt.get("tcp_flags", []))

    flow_tags = dedup_keep_order(flow_tags)
    packet_tags = dedup_keep_order(packet_tags)
    all_tags = dedup_keep_order(flow_tags + packet_tags)
    flow_observations = dedup_keep_order(flow_observations)
    packet_observations = dedup_keep_order(packet_observations)
    tcp_flags = dedup_keep_order(tcp_flags)

    printable_strings = clean_printable_strings(raw_printable_strings)
    payload_previews = clean_payload_previews(raw_payload_previews)

    combined_text_parts = []
    combined_text_parts.extend(flow_observations)
    combined_text_parts.extend(packet_observations)
    combined_text_parts.extend(printable_strings)
    combined_text_parts.extend(payload_previews[:10])

    combined_text = "\n".join([x for x in combined_text_parts if x])

    profile = {
        "flow_key": flow.get("flow_key"),
        "packet_count": flow.get("packet_count", 0),
        "total_bytes": flow.get("total_bytes", 0),
        "flow_tags": flow_tags,
        "packet_tags": packet_tags,
        "all_tags": all_tags,
        "flow_observations": flow_observations,
        "packet_observations": packet_observations,
        "payload_previews": payload_previews,
        "printable_strings": printable_strings,
        "tcp_flags": tcp_flags,
        "intervals": intervals,
        "combined_text": combined_text
    }
    return profile

